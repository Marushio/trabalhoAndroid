        View view = inflater.inflate(R.layout.fragment_list_tarefa, container, false);
        Tarefas = (ListView) view.findViewById(R.id.lvTarefa);
        TarefaTask tarefaTask = new TarefaTask();
        tarefaTask.TarefaTask(getContext(), Tarefas);
